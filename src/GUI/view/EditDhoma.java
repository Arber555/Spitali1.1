    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(txtNrDhomes.getText()!="" && txtNrShtratave.getText()!="")
        {
            DhomaForma.d.setNrdhomes(Integer.parseInt(txtNrDhomes.getText()));
            DhomaForma.d.setNrshtratve(Integer.parseInt(txtNrShtratave.getText()));
       
            String x = jComboBoxSektori.getSelectedItem().toString();
            String [] y = x.split("\\.");
            Sektori s = new Sektori();
            s.setEmri(y[1]);
            s.setId(Integer.parseInt(y[0]));
            DhomaForma.d.setSektoriID(s);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


