    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!(txtEmri.getText().isEmpty() || txtCmimi.getText().isEmpty() || txtApershkrimi.getText().isEmpty() || ComboBoxDita.getSelectedIndex()<0 || ComboBoxMuji.getSelectedIndex()<0 || ComboBoxViti.getSelectedIndex()<0))
        {
            b.setEmri(txtEmri.getText());
            b.setCmimi(Double.valueOf(txtCmimi.getText()));
            b.setPershkrimiKomponenteve(txtApershkrimi.getText());
            java.sql.Date ds = java.sql.Date.valueOf( ComboBoxViti.getSelectedItem().toString()+"-"+ComboBoxMuji.getSelectedItem().toString()+"-"+ComboBoxDita.getSelectedItem().toString() ); //"1995-12-30"
            b.setDataSkadimit(ds);
            
            try {
                if(br.insert(b))
                {
                    JOptionPane.showMessageDialog(null, "U regjistrua Barna!!!");
                }
            } catch (SpitaliException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Nuk u regjistrua Barna!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

