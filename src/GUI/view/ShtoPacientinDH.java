    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int x = jTablePacientat.getSelectedRow();
        
        if(x>-1)
        {
            if(DhomaMForma.d.getCountP() == DhomaMForma.d.getNrshtratve())
            {
                JOptionPane.showMessageDialog(null, "Dhoma eshte full");
            }
            else{
                p = ptm.getPacienti(x);

                p.setIDdhoma(DhomaMForma.d);

                try {
                    if(pr.update(p))
                    {
                        JOptionPane.showMessageDialog(null, "Eshte shtuar pacjenti ne dhomÃ«");
                        intiTablePDhoma(pr.findByDhoma(DhomaMForma.d));
                    }
                } catch (SpitaliException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Nuk keni selektuar asni pacient");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x = jTablePacientetDhom.getSelectedRow();
        
        if(x>-1)
        {
            p = ptm.getPacienti(x);
            
            p.setIDdhoma(null);
            
            int y = JOptionPane.showConfirmDialog(null, "A jeni i sigurt qe doni ta fshini!!!");
            
            if(y == 0)
            {
                try {
                    if(pr.update(p))
                    {
                        JOptionPane.showMessageDialog(null, "Eshte fshir pacient nga dhoma");
                        intiTablePDhoma(pr.findByDhoma(DhomaMForma.d));
                    }
                } catch (SpitaliException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Nuk eshte fshir asni pacient nga dhoma");
            }
                
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Nuk keni selektuar asni pacient");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


